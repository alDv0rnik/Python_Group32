# Сделайте так, чтобы все нечетные по порядку слева на право символы стали “_”, а все символы,
# местоположение которых четное и которые равны “a” - стали “b”. Например “Ham is tasty” => “_b _s_t_s_y”

# куда-то пропло начало строки в примере. И непонятно, нужно менять четные символы в результирующей или исходной строке?

# обработал исходную, так как все символы исходной строки в результирующей встанут на четные позиции,
# а значит все символы "а" можно менять без проверки

text = input("Enter your string: ")
res = ""
for i in range(len(text)):
    res += "_"
    if (i % 2) and text[i] == "a":
        res = res + "b"
    else:
        res = res + text[i]
print(res)
